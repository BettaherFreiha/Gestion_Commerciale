    private void NumFournisseurActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NumFournisseurActionPerformed
        int i=0;
        if (NumFournisseur.getSelectedIndex()!= -1) {
            while (Integer.parseInt(DB.fournisseurs.get(i).getNum()) != Integer.parseInt(NumFournisseur.getSelectedItem().toString())) i++;
            
            NomFournisseur.setText(DB.fournisseurs.get(i).getNom());
            Prenom.setText(DB.fournisseurs.get(i).getPrenom());
            Adresse.setText(DB.fournisseurs.get(i).getAdresse());
            Civilite.setSelectedItem(DB.fournisseurs.get(i).getCivilite());
            Societe.setText(DB.fournisseurs.get(i).getSociete());
            Email.setText(DB.fournisseurs.get(i).getEmail());
            CodePostal.setText(DB.fournisseurs.get(i).getCodePostal());
            Ville.setText(DB.fournisseurs.get(i).getVille());
            Pays.setText(DB.fournisseurs.get(i).getPays());
            Telephone.setText(DB.fournisseurs.get(i).getTelephone());
            Mobile.setText(DB.fournisseurs.get(i).getMobile());
            Fax.setText(DB.fournisseurs.get(i).getFax());
        }
        
    }//GEN-LAST:event_NumFournisseurActionPerformed

    private void ModifierClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierClientActionPerformed
        Fournisseur fournisseur = new Fournisseur();
        
        fournisseur.setNum(Integer.parseInt(NumFournisseur.getSelectedItem().toString()));
        fournisseur.setNomClient(NomFournisseur.getText());
        fournisseur.setPrenom(Prenom.getText());
        fournisseur.setAdresse(Adresse.getText());
        fournisseur.setCivilite(Civilite.getSelectedItem().toString());
        fournisseur.setSociete(Societe.getText());
        fournisseur.setEmail(Email.getText());
        fournisseur.setVille(Ville.getText());
        fournisseur.setPays(Pays.getText());
        fournisseur.setTelephone(Telephone.getText());
        fournisseur.setMobile(Mobile.getText());
        fournisseur.setFax(Fax.getText());
        fournisseur.setCodePostal(CodePostal.getText());
        DB.UpdateDB(fournisseur);
        Actualiser(NumFournisseur.getSelectedIndex());
         JOptionPane.showMessageDialog( null, "Succès!", 
					      "Fournisseur Modifié(e)", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_ModifierClientActionPerformed

