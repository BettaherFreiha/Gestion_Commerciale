    public void Actualiser(int j) {
        DB.SelectNumFournisseurDB();
        NumFournisseur.removeAllItems();
        for(int i=0; i<DB.fournisseurs.size(); i++) {
            NumFournisseur.addItem(DB.fournisseurs.get(i).getNum());
        }
        if(NumFournisseur.getItemCount() !=0) {
        
            NumFournisseur.setSelectedItem(DB.fournisseurs.get(j).getNum());
            NomFournisseur.setText(DB.fournisseurs.get(j).getNom());
            Prenom.setText(DB.fournisseurs.get(j).getPrenom());
            Adresse.setText(DB.fournisseurs.get(j).getAdresse());
            Civilite.setSelectedItem(DB.fournisseurs.get(j).getCivilite());
            Societe.setText(DB.fournisseurs.get(j).getSociete());
            Email.setText(DB.fournisseurs.get(j).getEmail());
            Ville.setText(DB.fournisseurs.get(j).getVille());
            Pays.setText(DB.fournisseurs.get(j).getPays());
            Telephone.setText(DB.fournisseurs.get(j).getTelephone());
            Mobile.setText(DB.fournisseurs.get(j).getMobile());
            Fax.setText(DB.fournisseurs.get(j).getFax());
            CodePostal.setText(DB.fournisseurs.get(j).getCodePostal());
        }
    }

